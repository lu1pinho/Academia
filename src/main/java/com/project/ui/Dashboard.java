    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String cpf = jTextField5.getText();

        // Verificar se já existe aluno com o CPF informado ou Nome e buscar no banco
        if (!cpf.isEmpty()) {
            // Buscar por CPF
            AlunoDAO alunoDAO = new AlunoDAO();
            try {
                Aluno aluno = alunoDAO.buscarAluno(cpf);
                
                String email_text = aluno.getEmail();
                
                jLabel12.setText("Nome Completo: " + aluno.getNome());
                jLabel13.setText("CPF: " + aluno.getCpf());
                jLabel14.setText("Data de Nascimento: " + aluno.getNascimento());
                jLabel15.setText("Email: " + email_text);
                jLabel16.setText("Telefone: " + aluno.getTelefone());
                jLabel17.setText("Plano Atual: " + aluno.getPlano());
                jLabel18.setText("Status: " + aluno.getStatus());
                jLabel19.setText("Encerra em: " + aluno.getDatafim());
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Erro ao buscar aluno: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Informe um CPF para buscar", "Erro", JOptionPane.ERROR_MESSAGE);
        }




    }//GEN-LAST:event_jButton2ActionPerformed

