    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        AlunoDAO alunoDAO = new AlunoDAO();

        String nome = jTextField1.getText();
        String cpf = jTextField2.getText();
        String email = jTextField3.getText();
        String telefone = jTextField4.getText();
        String dia = jComboBox1.getSelectedItem().toString();
        String mes = verificarMes(jComboBox2.getSelectedItem().toString());
        String ano = jComboBox3.getSelectedItem().toString();
        String plano = jComboBox4.getSelectedItem().toString();

        String dataNascimento = dia + "/" + mes + "/" + ano;

        if(nome.isEmpty() || cpf.isEmpty() || email.isEmpty() || telefone.isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatórios", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Aluno aluno = new Aluno(nome, cpf, dataNascimento, email, telefone, plano);
        try {
            // Verifica se o aluno já existe antes de tentar salvar
            if (!alunoDAO.existeAluno(nome, cpf, telefone)) {
                alunoDAO.salvar(aluno);
                JOptionPane.showMessageDialog(null, "Aluno salvo com sucesso!");
                carregarDados();
                // Limpar campos após salvar
                limparCampos();
            } else {
                JOptionPane.showMessageDialog(null, "Aluno já existe com o CPF informado.", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Erro ao salvar aluno: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

