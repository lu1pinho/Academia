    private void carregarDados() {
        AlunoDAO alunoDAO = new AlunoDAO();
        List<Aluno> alunos;
        
        try {
            alunos = alunoDAO.buscarTodosAlunos();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao buscar alunos: " + e.getMessage());
            return;
        }
        
        String[] colunas = {"Nome", "CPF", "Data de Nascimento", "Email", "Telefone", "Plano", "Status", "Data Inicial", "Data Final"};
        DefaultTableModel modelo = new DefaultTableModel(colunas, 0);

        for (Aluno aluno : alunos) {
            Object[] linha = {
                aluno.getNome(),
                aluno.getCpf(),
                aluno.getNascimento(),
                aluno.getEmail(),
                aluno.getTelefone(),
                aluno.getPlano(),
                aluno.getStatus(),
                aluno.getData_inicio(),
                aluno.getDatafim()
            };
            modelo.addRow(linha);
        }

        jTable1.setModel(modelo);
    }
    
